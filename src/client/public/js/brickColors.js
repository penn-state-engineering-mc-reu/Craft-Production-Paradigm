window.BrickColors.spectrumPalette = [];
window.BrickColors.allPaletteData.forEach(paletteRow => {
    let spectrumPaletteRow = [];
   paletteRow.forEach(value => {
       spectrumPaletteRow.push(value.RGBString);
   });
   window.BrickColors.spectrumPalette.push(spectrumPaletteRow);
});

window.BrickColors.findByRGBString = function(str)
{
    let foundElem = null;

    window.BrickColors.allPaletteData.some(paletteRow => {
        return paletteRow.some(value => {
           if(value.RGBString === str)
           {
               foundElem = value;
               return true;
           }
           else
           {
               return false;
           }
        });
    });

    return foundElem;
};

window.BrickColors.findByColorID = function(colorID)
{
    let foundElem = null;

    window.BrickColors.allPaletteData.some(paletteRow => {
        return paletteRow.some(value => {
            if(value.colorID === colorID)
            {
                foundElem = value;
                return true;
            }
            else
            {
                return false;
            }
        });
    });

    return foundElem;
};

window.BrickColors.defaultBrickColor = window.BrickColors.findByColorID(194 /* Medium stone grey */);